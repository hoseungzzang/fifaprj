package com.fifa.web.bean;

public class PlayerBean {
    
	private int pindex;
	private String name;
	private char signUp;
	

	public int getPindex() {
		return pindex;
	}
	public void setPindex(int pindex) {
		this.pindex = pindex;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public char getSignUp() {
		return signUp;
	}
	public void setSignUp(char signUp) {
		this.signUp = signUp;
	}
    
    
}
